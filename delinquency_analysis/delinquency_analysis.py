class RiskEstimationLogger:
    """Captures print statements and saves them to markdown and HTML files."""
    
    def __init__(self):
        self.output_dir = os.path.join(os.path.dirname(__file__), '..', 'risk_estimate_outputs')
        os.makedirs(self.output_dir, exist_ok=True)
        
        self.timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        self.session_id = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        
        self.md_file = os.path.join(self.output_dir, "risk_estimate_session.md")
        self.html_file = os.path.join(self.output_dir, "risk_estimate_session.html")
        self.txt_file = os.path.join(self.output_dir, "risk_estimate_session.txt")
        
        # Initialize markdown and text files
        self._initialize_markdown()
        self._initialize_text()
        
        # Store original print function
        self.original_print = print
        
        # Replace built-in print with our logger
        import builtins
        builtins.print = self.log_print
    
    def _initialize_markdown(self):
        """Initialize the markdown file with header (overwrite existing)."""
        with open(self.md_file, 'w', encoding='utf-8') as f:
            f.write(f"""# Risk Estimation Session Report

**Date:** {self.session_id}  
**Session ID:** {self.timestamp}  
**Generated by:** Delinquency Data Management System (DDMS)

---

""")
    
    def _initialize_text(self):
        """Initialize the text file with header (overwrite existing)."""
        with open(self.txt_file, 'w', encoding='utf-8') as f:
            f.write(f"""Risk Estimation Session Report

Date: {self.session_id}
Session ID: {self.timestamp}
Generated by: Delinquency Data Management System (DDMS)

{'-' * 50}

""")

    def log_print(self, *args, sep=' ', end='\n', file=None, flush=False):
        """Custom print function that logs to console, markdown and text files."""
        # Call original print for console output
        self.original_print(*args, sep=sep, end=end, file=file, flush=flush)
        
        # Format the message
        message = sep.join(str(arg) for arg in args) + end
        
        # Append to text file (simple, unformatted)
        with open(self.txt_file, 'a', encoding='utf-8') as f:
            f.write(message)
        
        # Append to markdown file
        with open(self.md_file, 'a', encoding='utf-8') as f:
            message_stripped = message.strip()
            
            # Skip empty messages
            if not message_stripped:
                f.write(message)
                return
            
            # Format for markdown based on content patterns
            if message_stripped.startswith('='):
                # Header lines with equals signs
                header_text = message_stripped.replace('=', '').strip()
                if header_text:  # Only create header if there's actual content
                    f.write(f"\n## {header_text}\n\n")
                else:
                    f.write("\n---\n\n")  # Use horizontal rule for separator lines
                    
            elif message_stripped.startswith('PERFORMANCE METRICS'):
                # Performance metrics header
                f.write(f"\n### {message_stripped}\n\n")
                
            elif message_stripped.startswith('-') and len(set(message_stripped)) <= 2:
                # Lines that are just dashes (separators) - convert to horizontal rules
                f.write("\n---\n\n")
                
            elif any(title in message_stripped for title in [
                'Top 20 Most Influential Features',
                'Feature Importance by Data',
                'BEST MODEL SUMMARY',
                'Starting Comprehensive',
                'Delinquency analysis complete',
                'COMPREHENSIVE DELINQUENCY ANALYSIS REPORT'
            ]):
                # Important section headers
                f.write(f"\n## {message_stripped.rstrip(':')}\n\n")
                
            elif any(status in message_stripped for status in [
                'Feature engineering complete',
                'ML dataset prepared'
            ]):
                # Status messages - make bold and ensure separate lines
                f.write(f"**{message_stripped}**\n\n")
                
            elif message_stripped.endswith(':') and any(section in message_stripped for section in [
                'Dataset Overview', 'Model Performance Summary', 'Risk Score Distribution',
                'Risk Score Validation', 'Sample', 'Risk Factors for Delinquency'
            ]):
                # Report subsection headers
                f.write(f"\n### {message_stripped.rstrip(':')}\n\n")
                
            elif message_stripped.startswith('- ') or (
                message_stripped.startswith(' ') and message_stripped.lstrip().startswith('-')
            ):
                # List items (bullet points)
                clean_item = message_stripped.lstrip()
                f.write(f"{clean_item}\n")
                
            elif any(table_indicator in message_stripped for table_indicator in [
                'Model', '----', 'Random Forest', 'Gradient Boosting', 
                'Logistic Regression', 'SVC'
            ]) and ('----' in message_stripped or 
                   any(model in message_stripped for model in ['Random Forest', 'Gradient Boosting', 'Logistic Regression', 'SVC'])):
                # Table content (model comparison tables) - format as code block for alignment  
                if '----' in message_stripped:
                    f.write(f"\n```\n{message_stripped}\n")
                elif any(model in message_stripped for model in ['Random Forest', 'Gradient Boosting', 'Logistic Regression', 'SVC']):
                    f.write(f"{message_stripped}\n")
                    if 'SVC' in message_stripped:  # Last model in table
                        f.write("```\n\n")
                else:
                    f.write(f"{message_stripped}\n")
                
            elif message.startswith('  ') and not message.startswith('   '):
                # Indented content (2 spaces) - check for performance metrics
                clean_text = message.strip()
                # Remove bold formatting for processing
                clean_text_no_bold = clean_text.replace('**', '')
                
                # Always check if this could be a performance metric, regardless of table state
                if (':' in clean_text_no_bold and any(metric in clean_text_no_bold.lower() for metric in [
                    'accuracy', 'precision', 'recall', 'f1', 'auc'])):
                    
                    # If we're in a performance table, format as table row
                    if hasattr(self, '_in_performance_table') and self._in_performance_table:
                        parts = clean_text_no_bold.split(':', 1)
                        if len(parts) == 2:
                            metric = parts[0].strip()
                            value = parts[1].strip()
                            f.write(f"| {metric} | {value} |\n")
                        else:
                            # Could not split properly, write as is
                            f.write(f"  {message}")
                    
                    # If we're in a best model table, format as table row
                    elif hasattr(self, '_in_best_model_table') and self._in_best_model_table:
                        parts = clean_text_no_bold.split(':', 1)
                        if len(parts) == 2:
                            metric = parts[0].strip()
                            value = parts[1].strip()
                            # Make metric names bold for better formatting
                            f.write(f"| **{metric}** | {value} |\n")
                        else:
                            # Could not split properly, write as is
                            f.write(f"  {message}")
                    
                    else:
                        # Outside performance/best model table, format as bullet
                        f.write(f"- {clean_text}\n")
                
                # Handle other CV metrics  
                elif ':' in clean_text and 'cv' in clean_text.lower():
                    if hasattr(self, '_in_performance_table') and self._in_performance_table:
                        # End table if we have CV metrics outside the table context
                        f.write(f"\n")
                        self._in_performance_table = False
                    if hasattr(self, '_in_best_model_table') and self._in_best_model_table:
                        # End best model table if we have CV metrics outside the table context
                        f.write(f"\n")
                        self._in_best_model_table = False
                    f.write(f"- {clean_text}\n")
                else:
                    # End performance table if we're in one and this doesn't look like a metric
                    if hasattr(self, '_in_performance_table') and self._in_performance_table and not any(metric in clean_text_no_bold.lower() for metric in ['accuracy', 'precision', 'recall', 'f1', 'auc']):
                        f.write(f"\n")
                        self._in_performance_table = False
                    # End best model table if we're in one and this doesn't look like a metric
                    if hasattr(self, '_in_best_model_table') and self._in_best_model_table and not any(metric in clean_text_no_bold.lower() for metric in ['accuracy', 'precision', 'recall', 'f1', 'auc']):
                        f.write(f"\n")
                        self._in_best_model_table = False
                    # Regular indented content
                    f.write(f"  {message}\n")
                    
            elif message_stripped.startswith('    '):
                # Deeply indented content (4+ spaces) - check for classification reports
                clean_text = message_stripped.strip()
                
                # Check if we're in a classification report and this is report content
                if (hasattr(self, '_in_classification_report') and self._in_classification_report and 
                    (('precision' in clean_text.lower() and 'recall' in clean_text.lower() and 'f1' in clean_text.lower()) or
                     any(label in clean_text.lower() for label in ['macro avg', 'weighted avg', 'accuracy', 'support']) or
                     # Check if it looks like a classification report row
                     (len(clean_text.split()) >= 3 and any(char.replace('.', '').replace(' ', '').isdigit() for char in clean_text.split())) or
                     # Empty lines or formatting lines
                     clean_text == '' or all(c in ' -' for c in clean_text) or
                     # Class labels with numbers (like "0" and "1" classes)
                     (len(clean_text.split()) >= 4 and clean_text.split()[0].isdigit()))):
                    # Add to classification report code block
                    f.write(f"{clean_text}\n")
                
                # Check if it starts a confusion matrix
                elif clean_text.startswith('[[') or (clean_text.startswith('[') and ']' in clean_text):
                    if hasattr(self, '_in_classification_report') and self._in_classification_report:
                        # End the classification report code block, add confusion matrix header
                        f.write("```\n\n")
                        f.write("#### Confusion Matrix:\n\n")
                        f.write(f"{clean_text}\n")
                        
