            risk_scores[medium_prob_indices[:n_medium]] = 1
            
            final_unique, final_counts = np.unique(risk_scores, return_counts=True)
            print(f"Adjusted KNN distribution: {dict(zip(final_unique.astype(int), final_counts))}")
